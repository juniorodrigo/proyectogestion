    private void botonAgregarprodActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarprodActionPerformed
        // TODO add your handling code here:
        if(txtPrecio.getText().isEmpty() | txtRegistroSanitario.getText().isEmpty()| txtStock.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Existen campos vacíos");
        }
        else
        {
            boolean datosCorrectos = false;
            String mensajeX = "";
            try {
                int precio = Integer.parseInt(txtPrecio.getText());
                int stock = Integer.parseInt(txtStock.getText());
                int registro = Integer.parseInt(txtRegistroSanitario.getText());
                if(precio + stock + registro >=0) datosCorrectos = true;
                System.out.println("CORRECTO");
                    
            } catch (Exception e) {
                mensajeX = e.getMessage();
                // JOptionPane.showMessageDialog(null, e.getMessage()); 
            }
            if(datosCorrectos)
            {
                int codigo = 0;
                if(txtCodigo.getText()== "")
                {
                    codigo = Integer.parseInt(txtCodigo.getText());
                }
                Producto prod = new Producto();
                prod.setCodProducto(codigo);
                prod.setNombre(txtNombre.getText().toUpperCase());
                prod.setStock(Integer.parseInt(txtStock.getText()));
                prod.setPrecioUnitario(Double.parseDouble(txtPrecio.getText()));
                prod.setRegistroSanitario(Integer.parseInt(txtRegistroSanitario.getText()));
                String mensaje = pbo.agregarProducto(prod);
                JOptionPane.showMessageDialog(null, mensaje);
                limpiarCamposProducto();
                mostrarProducto();
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Algo ha salido mal - " + mensajeX);
            }
            
        }
    }//GEN-LAST:event_botonAgregarprodActionPerformed

