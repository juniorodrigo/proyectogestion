    private void botonAgregarEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarEmpActionPerformed
        // TODO add your handling code here:
        String canal = "";
        String puesto = "";
        canal = comboCanalVentaEmpleado.getSelectedItem().toString();
        puesto = comboPuestoEmpleado.getSelectedItem().toString();
        
        
        if(txtDniEmp.getText().isEmpty() | txtNombreEmp.getText().isEmpty() |txtCelularEmp.getText().isEmpty()|
                txtCorreoEmp.getText().isEmpty() | txtBreveteEmp.getText().isEmpty() | canal == "" | puesto == "")
        {
            JOptionPane.showMessageDialog(null, "Inserte toda la información necesaria");
        }
        else
        {
            boolean datosCorrectos = false;
            String mensajeX = "";
            try {
                int dni = Integer.parseInt(txtDniEmp.getText());
                int celular = Integer.parseInt(txtCelularEmp.getText());
                int brevete = Integer.parseInt(txtBreveteEmp.getText());
                if(dni + celular +brevete >=0) datosCorrectos = true;
                System.out.println("CORRECTO");
                    
            } catch (Exception e) {
                mensajeX = e.getMessage();
            }
            if(datosCorrectos)
            {
                int dni = 0;
                if(txtDniEmp.getText()!="")
                {
                    dni = Integer.parseInt(txtDniEmp.getText());
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "No se ha proporcionado un dni correcto");
                }
                System.out.println("DNI: "+dni);
                Empleado emp = new Empleado();
                emp.setDni(dni);
                emp.setNombre(txtNombreEmp.getText().toUpperCase());
                emp.setCelular(Integer.parseInt(txtCelularEmp.getText()));
                emp.setCorreo(txtCorreoEmp.getText().toUpperCase());
                emp.setBrevete(Integer.parseInt(txtBreveteEmp.getText()));
                emp.setCanalVenta(canal);
                emp.setPuesto(puesto);
                String mensaje = ebo.agregarEmpleado(emp);
                JOptionPane.showMessageDialog(null, mensaje);
                limpiarCamposEmpleado();
                mostrarEmpleado();
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Algo ha salido mal - " + mensajeX);
            }
            
        }
        
    }//GEN-LAST:event_botonAgregarEmpActionPerformed

